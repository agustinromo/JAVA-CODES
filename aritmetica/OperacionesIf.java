public class OperacionesIf{
 public static void main(String args[]){

   int operacion = 23, n1 = 8, n2 = 4, ans = 0;
   if(operacion == 1)
   {
     ans = n1 + n2;
     System.out.println("Ans " + ans);
    }
     else if(operacion == 2)
    {
      ans = n1 - n2;
      System.out.println("Ans " + ans);
      }
      else if(operacion == 3)
      {
        ans = n1 * n2;
        System.out.println("Ans " + ans);
      }
      else if(operacion == 4)
      {
        ans = n1 / n2;
        System.out.println("Ans " + ans);
      }
       else
       {
         System.out.println("CHUPA VERGA");
       }
 }
}
